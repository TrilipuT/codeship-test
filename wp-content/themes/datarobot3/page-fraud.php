                        function submitToSF() {
                            var sfReq = jQuery.ajax({
                                url: "https://fbnddub87j.execute-api.us-east-1.amazonaws.com/production/forms",
                                type: "POST",
                                headers: {
                                    "Accept": "application/json; charset=utf-8",
